/* Types and Description
 * Basic Types: arithmetic types, consist of two types(1) integer (2) floating point
 * Enumerated Types: still arithmetic types discrete integer values.
 * The type void: indicates that no value is available.
 * Derived Types: include (1) Pointer Types (2) Array Types, (3) Structure Types,
 * (4) Union Types (5) Function Types.
 */


/* Integer Types
 * standard integer types:
 * sizeof(char) = 1  (byte)         value ranges from -2^7 to +2^7-1 or, from 0 to 2^8-1
 * sizeof(unsighed char) = 1        value ranges from 0 to 2^8-1
 * sizeof(signed char) = 1          value ranges from -2^7 to 2^7-1
 * sizeof(int) = 2 or 4             value ranges from -2^15 to 2^15-1 or -2^31 to 2^31-1
 * sizeof(unsigned int) = 2 or 4    value ranges from 0 ro 2^16-1 or 0 to 2^32-1
 * sizeof(short) = 2                value ranges from -2^15 to 2^15-1
 * sizeof(unsigned short) = 2       value ranges from 0 to 2^16-1
 * sizeof(long) = 4                 value ranges from -2^31 to 2^31-1
 * sizeof(unsigned long) = 4        value ranges from 0 to 2^32-1
 */


/* Floating-Point Types
 * sizeof(float) = 4        value ranges from 1.2E-38 to 3.4E+38     precision 6 decimal places
 * sizeof(double) = 8       value ranges from 2.3E-308 to 1.7E+308   precision 15 decimal places
 * sizeof(long double) = 10 value ranges from 3.4E-4932 to 1.1E+4932 precision 19 decimal places
 */

